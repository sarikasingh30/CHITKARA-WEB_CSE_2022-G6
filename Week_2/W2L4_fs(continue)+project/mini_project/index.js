/*
• Base64 encoding is a way to convert binary data into a 
text string composed of 64 different ASCII characters.
• It is commonly used for encoding data for text-based
 communication protocols.
• The encoded data can be safely transmitted over media 
that are designed to deal with text.

Why ?
Binary data can be corrupted when transmitted over systems
 that are not 8-bit clean or where binary data can be
  misinterpreted. Base64 encoding ensures that the data 
  remains intact during transit by converting it to a 
  standard text format.
*/
